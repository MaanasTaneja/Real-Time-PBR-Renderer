	void loadLightPositions(vector<Light> lights)
	{
		for (int i = 0; i < lights.size(); i++)
		{
			lightPositions.push_back(lights.at(i).pos);
			lightColors.push_back(lights.at(i).col);
		}
	}

	void setupShaderUniforms(glm::mat4 projection_, glm::mat4 view_, Camera camera3) //temp to be shifted to masterrednerer
	{

		lampShader->use();
		lampShader->setMat4("projection", glm::mat4(projection_));
		lampShader->setMat4("view", glm::mat4(view_));
		lampShader->setVec3("skyColor", glm::vec3(1.0f, 1.0f, 1.0f));

	}

	void setupModelMatrix(int Index)  //SETS UP ONLY MODEL BOI
	{
		lampShader->use();
		glm::mat4 model = glm::mat4(1.0f);
		model = glm::translate(model, lightPositions.at(Index));
		model =  glm::scale(model, glm::vec3(0.1f));
		lampShader->setVec3("color", glm::vec3(lightColors.at(Index)));
		lampShader->setMat4("model", model);
	}

	void init(Shader* lShader)
	{
		lampShader = lShader; //change this to init
	}

	Shader* getShader()
	{
		return lampShader;
	}

	void renderLights(glm::mat4 proj, glm::mat4 viewM, Camera camera2)
	{

		setupShaderUniforms(proj, viewM, camera2);
		lampShader->use();
		for (int i = 0; i < lightPositions.size(); i++)
		{
			if (glm::distance(lightPositions.at(i), camera2.getPosition()) > 100.0)
			{
				continue;
			}
			glBindVertexArray(cubeVAO);
			setupModelMatrix(i);
			glDrawArrays(GL_TRIANGLES, 0, 36);

		}
	}
	unsigned int cube()
	{
		
		glGenVertexArrays(1, &cubeVAO);
		glGenBuffers(1, &cubeVBO);

		glBindVertexArray(cubeVAO);
		glBindBuffer(GL_ARRAY_BUFFER, cubeVBO);
		glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);

		glBindVertexArray(cubeVAO);
		glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
		glEnableVertexAttribArray(0);

		glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
		glEnableVertexAttribArray(1);

		glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
		glEnableVertexAttribArray(2);


		return cubeVAO;
	}

